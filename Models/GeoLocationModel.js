import mongoose from "mongoose";
const GeoLocationSchema = new mongoose.Schema({
    name: String,
    location: {
        type: {
            type: String, // Don't do `{ location: { type: String } }`
            enum: ['Point'], // 'location.type' must be 'Point'
            required: true
        },
        coordinates: {
            type: [Number],
            required: true
        }
    }
});

export default GeoLocationSchema